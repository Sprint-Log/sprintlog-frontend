<script lang="ts">
	let priority = '游릭';
	let priorities = ['游댮', '游리', '游릭'];
	type PriorityObject = {
		[key: string]: string;
	};
	let priorityLbls: PriorityObject = {
		'游댮': 'Urgent',
		'游리': 'Important',
		'游릭': 'Normal'
	};
	function selectColor(newColor: string) {
		priority = newColor;
	}
</script>

<span class="hidden md:visible">Priority:</span>
{#each priorities as c}
	<span
		class="chip {priority === c ? 'variant-filled-warning' : 'variant-soft'}"
		on:click={() => {
			selectColor(c);
		}}
		on:keypress
	>
		{c}
		{priorityLbls[c]}
	</span>
{/each}
