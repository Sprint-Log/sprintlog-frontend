<script lang="ts">
	export let progress = '游릳游릳游릳';
	let progresses = ['游릳游릳游릳', '游릴游릳游릳', '游릴游릴游릳', '游릴游릴游릴'];
	function setProgress(newColor: string) {
		progress = newColor;
	}
</script>

<span class="hidden md:visible">Progress:</span>
{#each progresses as p}
	<span
		class="chip {progress === p ? 'variant-filled-surface' : 'variant-soft'}"
		on:click={() => {
			setProgress(p);
		}}
		on:keypress
	>
		<span>{p}</span>
	</span>
{/each}
